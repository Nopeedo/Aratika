/**
 * enrich-mps-wikipedia.mjs — fill MP biographies from Wikipedia.
 *
 * For every MP, fetch the lead summary of their Wikipedia article (CC BY-SA) and
 * store it as a bio. Used ONLY where no curated parliament.nz bio exists, and
 * attributed to Wikipedia on the profile. We validate the match looks like an NZ
 * politician to avoid grabbing the wrong person; uncertain matches are skipped.
 *
 * Run:  node scripts/enrich-mps-wikipedia.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { execFileSync } from 'node:child_process'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const SRC = join(__dirname, '..', 'src', 'constants', 'mps-generated.ts')
const OUT = join(__dirname, '..', 'src', 'constants', 'mps-wikipedia.ts')
const UA = 'Mozilla/5.0 (Aratika civic info; +https://aratika.nz)'

// Extract {slug, name} for every MP from the generated source.
const text = readFileSync(SRC, 'utf8')
const re = /\{[^{}]*?slug:\s*'([^']+)'[^{}]*?\bname:\s*'([^']+)'[^{}]*?\}/g
const mps = []
const seen = new Set()
let m
while ((m = re.exec(text))) { if (!seen.has(m[1])) { seen.add(m[1]); mps.push({ slug: m[1], name: m[2] }) } }
console.log(`Found ${mps.length} MPs in mps-generated.ts\n`)

function wiki(title) {
  const url = `https://en.wikipedia.org/api/rest_v1/page/summary/${encodeURIComponent(title)}?redirect=true`
  try {
    const out = execFileSync('curl', ['-s', '--max-time', '20', '-A', UA, url], { encoding: 'utf8', maxBuffer: 16 * 1024 * 1024 })
    return JSON.parse(out)
  } catch { return null }
}
function looksNZpol(d) {
  if (!d || !d.extract || d.type === 'disambiguation') return false
  const t = `${d.description || ''} ${d.extract}`.toLowerCase()
  return /\bnew zealand\b/.test(t) && /politic|member of parliament|\bmp\b|minister|electorate|parliament/.test(t)
}

const result = {}
let ok = 0
for (const { slug, name } of mps) {
  let d = wiki(name)
  if (!looksNZpol(d)) d = wiki(`${name} (New Zealand politician)`)
  if (!looksNZpol(d)) d = wiki(`${name} (politician)`)
  if (!looksNZpol(d)) { console.warn(`○ ${slug} — no confident match`); continue }
  let bio = d.extract.replace(/\s+/g, ' ').trim()
  if (bio.length > 600) bio = bio.slice(0, 600).replace(/\s+\S*$/, '') + '…'
  const wikipediaUrl = d.content_urls?.desktop?.page || `https://en.wikipedia.org/wiki/${encodeURIComponent(d.title)}`
  result[slug] = { bio, wikipediaUrl }
  ok++
  console.log(`✓ ${slug}`)
}

const banner = `// AUTO-GENERATED by scripts/enrich-mps-wikipedia.mjs. Do not edit by hand.
// Bios = the lead summary of each MP's Wikipedia article (CC BY-SA 4.0), used
// ONLY where no curated parliament.nz bio exists, and attributed on the profile.
// Auto-matched — spot-check for the rare wrong-person match.\n`
writeFileSync(OUT, `${banner}\nexport const MP_WIKI: Record<string, { bio: string; wikipediaUrl: string }> = ${JSON.stringify(result, null, 2)}\n`, 'utf8')
console.log(`\nWrote ${ok}/${mps.length} Wikipedia bios → ${OUT}`)
