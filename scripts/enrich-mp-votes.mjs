/**
 * enrich-mp-votes.mjs — MP voting records from voted.nz (an independent compiler
 * of the official Parliamentary record / divisions, Office of the Clerk &
 * Parliamentary Service).
 *
 * Reads voted.nz's sitemap for every /people/ page, matches each to our MP by
 * name (diacritic-insensitive token set), fetches their page, and extracts each
 * division they voted in (date, bill, for/against, outcome). Stores the current
 * term's votes for the profile "Voting record" section, attributed to voted.nz.
 *
 * Run:  node scripts/enrich-mp-votes.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { execFileSync } from 'node:child_process'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { parse } from 'node-html-parser'

const __dirname = dirname(fileURLToPath(import.meta.url))
const GEN = join(__dirname, '..', 'src', 'constants', 'mps-generated.ts')
const OUT = join(__dirname, '..', 'src', 'constants', 'mps-votes.ts')
const UA = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 Chrome/124 Safari/537.36'
const SITEMAP = 'https://voted.nz/sitemap.xml'
const PER_MP_LIMIT = 30        // most-recent N current-term votes kept per MP
const TERM_FROM_YEAR = 2023    // 54th Parliament

const curl = (url) => execFileSync('curl', ['-s', '-L', '--max-time', '30', '-A', UA, url], { encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 })

// name → diacritic-insensitive sorted token key (order/format independent)
const tokenKey = (s) => s.normalize('NFD').replace(/[̀-ͯ]/g, '').toLowerCase().replace(/['’`]/g, '').split(/[\s\-]+/).filter(Boolean).sort().join('|')

// Build our-MP lookup: tokenKey -> slug
const src = readFileSync(GEN, 'utf8')
const keyToSlug = new Map()
const slugRe = /slug:\s*'([^']+)'/g
let sm
while ((sm = slugRe.exec(src))) {
  const win = src.slice(sm.index, sm.index + 400)
  const nm = win.match(/\bname:\s*'([^']+)'/)
  if (nm) keyToSlug.set(tokenKey(nm[1]), sm[1])
}
keyToSlug.set(tokenKey('Christopher Luxon'), 'christopher-luxon')
keyToSlug.set(tokenKey('Judith Collins'), 'judith-collins')
console.log(`Indexed ${keyToSlug.size} MPs`)

// voted.nz person pages from the sitemap
const people = [...curl(SITEMAP).matchAll(/https:\/\/voted\.nz\/people\/([^<\/]+)\/?/g)].map((m) => m[1])
console.log(`voted.nz lists ${people.length} people`)

function parseVotes(html) {
  const root = parse(html)
  const out = []
  for (const li of root.querySelectorAll('li.division-vote')) {
    const divs = li.querySelectorAll('div')
    const date = (divs[1]?.text || '').trim()
    const title = (li.querySelector('.w3-large')?.text || '').trim()
    const voteSpan = li.querySelector('span.for, span.against, span.abstain')
    const vote = voteSpan ? voteSpan.text.trim() : (/\bvoted\b([^<]*)/i.exec(li.text)?.[1] || '').trim()
    const outcomeSpan = li.querySelector('span.passed, span.failed')
    const outcome = (outcomeSpan?.text || '').trim()
    const href = li.querySelector('a')?.getAttribute('href') || ''
    const year = Number((date.match(/\b(19|20)\d{2}\b/) || [])[0])
    if (!title || !date) continue
    out.push({ date, title, vote, outcome, url: href ? `https://voted.nz${href}` : '', year })
  }
  return out
}

const result = {}
let ok = 0, miss = 0
for (const personSlug of people) {
  const slug = keyToSlug.get(tokenKey(personSlug.replace(/\//g, '')))
  if (!slug) { miss++; continue }
  let votes
  try { votes = parseVotes(curl(`https://voted.nz/people/${personSlug}/`)) } catch { continue }
  if (votes.length === 0) continue
  const totalRecorded = votes.length
  const recent = votes.filter((v) => v.year >= TERM_FROM_YEAR).slice(-PER_MP_LIMIT).reverse()
  result[slug] = {
    votes: recent.map(({ date, title, vote, outcome, url }) => ({ date, title, vote, outcome, url })),
    totalRecorded,
    sourceUrl: `https://voted.nz/people/${personSlug}/`,
  }
  ok++
  if (ok % 20 === 0) console.log(`  …${ok} done`)
}

const banner = `// AUTO-GENERATED by scripts/enrich-mp-votes.mjs. Do not edit by hand.
// MP voting records from voted.nz (independent compiler of the official NZ
// Parliamentary record / divisions). Current-term votes kept per MP; full record
// linked. Attributed to voted.nz on the profile.\n`
writeFileSync(OUT, `${banner}\nexport interface MPVote { date: string; title: string; vote: string; outcome: string; url: string }\nexport interface MPVotes { votes: MPVote[]; totalRecorded: number; sourceUrl: string }\nexport const MP_VOTES: Record<string, MPVotes> = ${JSON.stringify(result, null, 2)}\n`, 'utf8')
console.log(`Matched ${ok} MPs (${miss} voted.nz people unmatched) → ${OUT}`)
